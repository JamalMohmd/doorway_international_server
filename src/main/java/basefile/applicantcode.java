package basefile;

public class applicantcode {

	String code;
	String userid;
	String key;
	
	public void setCode(String code)
	{
		this.code=code;
	}
	public String getCode()
	{
		return this.code;
	}
	public void setUserid(String userid)
	{
		this.userid=userid;
	}
	public String getUserid()
	{
		return this.userid;
	}
	public void setKey(String key)
	{
		this.key=key;
	}
	public String getKey()
	{
		return this.key;
	}
}
